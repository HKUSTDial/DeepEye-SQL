    SELECT 
        "USER_PSEUDO_ID",
        SUM(CASE WHEN "EVENT_NAME" = 'page_view' THEN 1 ELSE 0 END) AS pageviews,
        MAX(CASE WHEN "EVENT_NAME" = 'purchase' THEN 1 ELSE 0 END) AS is_purchaser
    FROM all_events
    GROUP BY "USER_PSEUDO_ID"
)
SELECT 
    AVG(CASE WHEN is_purchaser = 1 THEN pageviews END) AS avg_pageviews_purchasers,
    AVG(CASE WHEN is_purchaser = 0 THEN pageviews END) AS avg_pageviews_non_purchasers,
    avg_pageviews_purchasers - avg_pageviews_non_purchasers AS avg_difference
FROM user_stats