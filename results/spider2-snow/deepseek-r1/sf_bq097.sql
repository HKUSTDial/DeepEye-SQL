SELECT 
    "GeoName",
    MAX(CASE WHEN EXTRACT(YEAR FROM "Year") = 2017 THEN "Earnings_per_job_avg" END) - MAX(CASE WHEN EXTRACT(YEAR FROM "Year") = 2012 THEN "Earnings_per_job_avg" END) AS increasing_amount
FROM "SDOH"."SDOH_BEA_CAINC30"."FIPS"
WHERE "GeoName" LIKE '%, MA' AND EXTRACT(YEAR FROM "Year") IN (2012, 2017)
GROUP BY "GeoName"
HAVING MAX(CASE WHEN EXTRACT(YEAR FROM "Year") = 2012 THEN "Earnings_per_job_avg" END) IS NOT NULL AND MAX(CASE WHEN EXTRACT(YEAR FROM "Year") = 2017 THEN "Earnings_per_job_avg" END) IS NOT NULL
ORDER BY "GeoName"