event_params_extracted AS (
    SELECT 
        de.EVENT_DATE,
        de.USER_PSEUDO_ID,
        de.EVENT_TIMESTAMP,
        MAX(CASE WHEN ep.value:"key"::STRING = 'ga_session_id' THEN COALESCE(ep.value:"value"."string_value"::STRING, CAST(ep.value:"value"."int_value" AS STRING)) END) AS session_id,
        MAX(CASE WHEN ep.value:"key"::STRING = 'source' THEN ep.value:"value"."string_value"::STRING END) AS source,
        MAX(CASE WHEN ep.value:"key"::STRING = 'medium' THEN ep.value:"value"."string_value"::STRING END) AS medium,
        MAX(CASE WHEN ep.value:"key"::STRING = 'campaign' THEN ep.value:"value"."string_value"::STRING END) AS campaign
    FROM december_events de,
    LATERAL FLATTEN(INPUT => de.EVENT_PARAMS) ep
    GROUP BY de.EVENT_DATE, de.USER_PSEUDO_ID, de.EVENT_TIMESTAMP
),
sessions_with_channel AS (
    SELECT 
        session_id,
        USER_PSEUDO_ID,
        source,
        medium,
        campaign,
        CASE
            WHEN source = '(direct)' AND medium IN ('(not set)', '(none)') THEN 'Direct'
            WHEN campaign LIKE '%cross-network%' THEN 'Cross-network'
            WHEN (LOWER(source) IN ('alibaba','amazon','google shopping','shopify','etsy','ebay','stripe','walmart') OR REGEXP_LIKE(campaign, '^(.*(([^a-df-z]|^)shop|shopping).*)$', 'i')) AND REGEXP_LIKE(medium, '^(.*cp.*|ppc|retargeting|paid.*)$', 'i') THEN 'Paid Shopping'
            WHEN LOWER(source) IN ('baidu','bing','duckduckgo','ecosia','google','yahoo','yandex') AND REGEXP_LIKE(medium, '^(.*cp.*|ppc|paid.*)$', 'i') THEN 'Paid Search'
            WHEN REGEXP_LIKE(LOWER(source), 'badoo|facebook|fb|instagram|linkedin|pinterest|tiktok|twitter|whatsapp', 'i') AND REGEXP_LIKE(medium, '^(.*cp.*|ppc|retargeting|paid.*)$', 'i') THEN 'Paid Social'
            WHEN LOWER(source) IN ('dailymotion','disneyplus','netflix','youtube','vimeo','twitch') AND REGEXP_LIKE(medium, '^(.*cp.*|ppc|retargeting|paid.*)$', 'i') THEN 'Paid Video'
            WHEN LOWER(medium) IN ('display','banner','expandable','interstitial','cpm') THEN 'Display'
            WHEN LOWER(source) IN ('alibaba','amazon','google shopping','shopify','etsy','ebay','stripe','walmart') OR REGEXP_LIKE(campaign, '^(.*(([^a-df-z]|^)shop|shopping).*)$', 'i') THEN 'Organic Shopping'
            WHEN REGEXP_LIKE(LOWER(source), 'badoo|facebook|fb|instagram|linkedin|pinterest|tiktok|twitter|whatsapp', 'i') OR LOWER(medium) IN ('social','social-network','social-media','sm','social network','social media') THEN 'Organic Social'
            WHEN LOWER(source) IN ('dailymotion','disneyplus','netflix','youtube','vimeo','twitch') OR REGEXP_LIKE(medium, '^(.*video.*)$', 'i') THEN 'Organic Video'
            WHEN LOWER(source) IN ('baidu','bing','duckduckgo','ecosia','google','yahoo','yandex') OR LOWER(medium) = 'organic' THEN 'Organic Search'
            WHEN LOWER(medium) = 'referral' THEN 'Referral'
            WHEN LOWER(source) IN ('email','e-mail','e_mail','e mail') OR LOWER(medium) IN ('email','e-mail','e_mail','e mail') THEN 'Email'
            WHEN LOWER(medium) = 'affiliate' THEN 'Affiliates'
            WHEN LOWER(medium) = 'audio' THEN 'Audio'
            WHEN LOWER(source) = 'sms' OR LOWER(medium) = 'sms' THEN 'SMS'
            WHEN LOWER(medium) LIKE '%push%' OR LOWER(medium) LIKE '%mobile%' OR LOWER(medium) LIKE '%notification%' THEN 'Mobile Push Notifications'
            ELSE 'Unassigned'
        END AS channel
    FROM (
        SELECT 
            session_id,
            USER_PSEUDO_ID,
            source,
            medium,
            campaign,
            ROW_NUMBER() OVER (PARTITION BY session_id, USER_PSEUDO_ID ORDER BY EVENT_TIMESTAMP) as rn
        FROM event_params_extracted
        WHERE session_id IS NOT NULL
    ) first_event
    WHERE rn = 1
),
channel_counts AS (
    SELECT 
        channel,
        COUNT(*) as session_count
    FROM sessions_with_channel
    GROUP BY channel
),
ranked_channels AS (
    SELECT 
