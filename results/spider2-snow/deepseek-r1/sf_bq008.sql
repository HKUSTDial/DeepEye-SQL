  SELECT * FROM "GA360"."GOOGLE_ANALYTICS_SAMPLE"."GA_SESSIONS_20170130" UNION ALL
  SELECT * FROM "GA360"."GOOGLE_ANALYTICS_SAMPLE"."GA_SESSIONS_20170131"
),
qualified_sessions AS (
  SELECT 
    "fullVisitorId",
    "visitId",
    "date",
    "hits"
  FROM all_sessions
  WHERE "date" BETWEEN '20170101' AND '20170131'
    AND LOWER("trafficSource":"campaign"::STRING) LIKE '%data share%'
),
pageviews AS (
  SELECT 
    qs."fullVisitorId",
    qs."visitId",
    qs."date",
    hit.value:"hitNumber"::INTEGER AS hitNumber,
    hit.value:"time"::INTEGER AS hit_time_ms,
    hit.value:"page":"pagePath"::STRING AS pagePath
  FROM qualified_sessions qs,
  LATERAL FLATTEN(INPUT => qs."hits") hit
  WHERE hit.value:"type"::STRING = 'PAGE'
    AND hit.value:"page":"pagePath" IS NOT NULL
),
with_next AS (
  SELECT 
    "fullVisitorId",
    "visitId",
    "date",
    hitNumber,
    hit_time_ms,
    pagePath,
    LEAD(pagePath) OVER (PARTITION BY "fullVisitorId", "visitId" ORDER BY hitNumber) AS next_pagePath,
    LEAD(hit_time_ms) OVER (PARTITION BY "fullVisitorId", "visitId" ORDER BY hitNumber) AS next_hit_time_ms
  FROM pageviews
),
home_hits AS (
  SELECT 
    *,
    (next_hit_time_ms - hit_time_ms) / 1000.0 AS time_on_home_seconds
  FROM with_next
  WHERE pagePath LIKE '/home%'
    AND next_pagePath IS NOT NULL
)
SELECT 
  (SELECT next_pagePath FROM home_hits GROUP BY next_pagePath ORDER BY COUNT(*) DESC LIMIT 1) AS most_common_next_page,
  MAX(time_on_home_seconds) AS max_time_on_home_seconds
FROM home_hits