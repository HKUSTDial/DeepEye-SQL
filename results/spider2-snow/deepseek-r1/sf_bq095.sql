SELECT DISTINCT
    "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"."name",
    "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"."approvedSymbol",
    flattened_urls.value AS clinical_trial_links
FROM
    "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"
JOIN
    "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"
    ON "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"."drugId" = "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"."id"
LEFT JOIN LATERAL FLATTEN(INPUT => "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"."urls") AS flattened_urls
WHERE
    "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"."diseaseId" = 'EFO_0007416'
    AND "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"."status" = 'Completed'