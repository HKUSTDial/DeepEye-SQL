WITH home_depot AS (
  SELECT p."POI_ID", p."POI_NAME", a."LATITUDE", a."LONGITUDE"
  FROM "US_REAL_ESTATE"."CYBERSYN"."POINT_OF_INTEREST_INDEX" p
  INNER JOIN "US_REAL_ESTATE"."CYBERSYN"."POINT_OF_INTEREST_ADDRESSES_RELATIONSHIPS" r ON p."POI_ID" = r."POI_ID"
  INNER JOIN "US_REAL_ESTATE"."CYBERSYN"."US_ADDRESSES" a ON r."ADDRESS_ID" = a."ADDRESS_ID"
  WHERE p."POI_NAME" = 'The Home Depot'
), lowes AS (
  SELECT p."POI_ID", p."POI_NAME", a."LATITUDE", a."LONGITUDE"
  FROM "US_REAL_ESTATE"."CYBERSYN"."POINT_OF_INTEREST_INDEX" p
  INNER JOIN "US_REAL_ESTATE"."CYBERSYN"."POINT_OF_INTEREST_ADDRESSES_RELATIONSHIPS" r ON p."POI_ID" = r."POI_ID"
  INNER JOIN "US_REAL_ESTATE"."CYBERSYN"."US_ADDRESSES" a ON r."ADDRESS_ID" = a."ADDRESS_ID"
  WHERE p."POI_NAME" = 'Lowe''s Home Improvement'
)
SELECT hd."POI_ID" AS home_depot_id, hd."POI_NAME" AS home_depot_name, l."POI_ID" AS lowes_id, l."POI_NAME" AS lowes_name, ST_DISTANCE(ST_MAKEPOINT(hd."LONGITUDE", hd."LATITUDE"), ST_MAKEPOINT(l."LONGITUDE", l."LATITUDE")) / 1609 AS distance_miles
FROM home_depot hd
CROSS JOIN lowes l
QUALIFY ROW_NUMBER() OVER (PARTITION BY hd."POI_ID" ORDER BY distance_miles) = 1