product_revenue AS (
    SELECT 
        "trafficSource":"source"::STRING AS traffic_source,
        TO_DATE("date", 'YYYYMMDD') AS session_date,
        hit.value:"product" AS product_array
    FROM all_sessions,
    LATERAL FLATTEN(input => "hits") AS hit
),
product_revenue_unnested AS (
    SELECT 
        traffic_source,
        session_date,
        product.value:"productRevenue"::FLOAT AS product_revenue
    FROM product_revenue,
    LATERAL FLATTEN(input => product_array) AS product
    WHERE product_revenue IS NOT NULL AND product_revenue > 0
),
source_totals AS (
    SELECT 
        traffic_source,
        SUM(product_revenue) AS total_revenue
    FROM product_revenue_unnested
    GROUP BY traffic_source
    ORDER BY total_revenue DESC
    LIMIT 1
),
daily_revenue AS (
    SELECT 
        pr.traffic_source,
        pr.session_date,
        SUM(pr.product_revenue) AS daily_total
    FROM product_revenue_unnested pr
    WHERE pr.traffic_source = (SELECT traffic_source FROM source_totals)
    GROUP BY pr.traffic_source, pr.session_date
),
weekly_revenue AS (
    SELECT 
        pr.traffic_source,
        DATE_TRUNC('week', pr.session_date) AS week_start,
        SUM(pr.product_revenue) AS weekly_total
    FROM product_revenue_unnested pr
    WHERE pr.traffic_source = (SELECT traffic_source FROM source_totals)
    GROUP BY pr.traffic_source, week_start
),
monthly_revenue AS (
    SELECT 
        pr.traffic_source,
        DATE_TRUNC('month', pr.session_date) AS month_start,
        SUM(pr.product_revenue) AS monthly_total
    FROM product_revenue_unnested pr
    WHERE pr.traffic_source = (SELECT traffic_source FROM source_totals)
    GROUP BY pr.traffic_source, month_start
)
SELECT 
    (SELECT traffic_source FROM source_totals) AS top_traffic_source,
    MAX(daily_total / 1000000000000) AS max_daily_revenue_millions,
    MAX(weekly_total / 1000000000000) AS max_weekly_revenue_millions,
    MAX(monthly_total / 1000000000000) AS max_monthly_revenue_millions
FROM daily_revenue
CROSS JOIN weekly_revenue
CROSS JOIN monthly_revenue
GROUP BY top_traffic_source