SELECT
    "APP_GROUP_ID",
    CAST(TO_TIMESTAMP("TIME") AS DATE) AS "start_day",
    COUNT(DISTINCT "USER_ID") AS "unique_users"
FROM
    "BRAZE_USER_EVENT_DEMO_DATASET"."PUBLIC"."USERS_BEHAVIORS_APP_SESSIONSTART_VIEW"
WHERE
    CAST(TO_TIMESTAMP("TIME") AS DATE) BETWEEN DATE '2023-06-01' AND DATE '2023-06-07'
GROUP BY
    "APP_GROUP_ID",
    CAST(TO_TIMESTAMP("TIME") AS DATE)
ORDER BY
    "APP_GROUP_ID",
    "start_day"