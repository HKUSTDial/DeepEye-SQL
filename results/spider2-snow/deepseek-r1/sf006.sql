WITH "all_states" AS (
    SELECT "GEO_ID", "GEO_NAME"
    FROM "FINANCE__ECONOMICS"."CYBERSYN"."GEOGRAPHY_INDEX"
    WHERE "LEVEL" = 'State' AND "GEO_ID" LIKE 'geoId/%'
), "counts_20200301" AS (
    SELECT "ID_STATE", COUNT(*) AS "cnt"
    FROM "FINANCE__ECONOMICS"."CYBERSYN"."FINANCIAL_BRANCH_ENTITIES"
    WHERE "ID_COUNTRY" = 'country/USA' AND "CATEGORY" = 'Branch'
        AND "START_DATE" <= '2020-03-01'
        AND ("END_DATE" IS NULL OR "END_DATE" >= '2020-03-01')
    GROUP BY "ID_STATE"
), "counts_20211231" AS (
    SELECT "ID_STATE", COUNT(*) AS "cnt"
    FROM "FINANCE__ECONOMICS"."CYBERSYN"."FINANCIAL_BRANCH_ENTITIES"
    WHERE "ID_COUNTRY" = 'country/USA' AND "CATEGORY" = 'Branch'
        AND "START_DATE" <= '2021-12-31'
        AND ("END_DATE" IS NULL OR "END_DATE" >= '2021-12-31')
    GROUP BY "ID_STATE"
)
SELECT 
    s."GEO_ID" AS "ID_STATE",
    s."GEO_NAME" AS "STATE_NAME",
    COALESCE(c1."cnt", 0) AS "COUNT_MAR_2020",
    COALESCE(c2."cnt", 0) AS "COUNT_DEC_2021",
    CASE 
        WHEN COALESCE(c1."cnt", 0) = 0 THEN NULL
        ELSE (COALESCE(c2."cnt", 0) - COALESCE(c1."cnt", 0)) * 100.0 / COALESCE(c1."cnt", 0)
    END AS "PERCENTAGE_CHANGE"
FROM "all_states" s
LEFT JOIN "counts_20200301" c1 ON s."GEO_ID" = c1."ID_STATE"
LEFT JOIN "counts_20211231" c2 ON s."GEO_ID" = c2."ID_STATE"
ORDER BY s."GEO_ID"