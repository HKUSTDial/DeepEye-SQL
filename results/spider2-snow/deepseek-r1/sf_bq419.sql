WITH all_events AS (
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1980"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1981"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1982"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1983"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1984"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1985"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1986"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1987"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1988"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1989"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1990"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1991"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1992"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1993"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1994"
    UNION ALL
    SELECT "state", EXTRACT(YEAR FROM TO_TIMESTAMP("event_begin_time" / 1000000)) AS event_year FROM "NOAA_DATA"."NOAA_HISTORIC_SEVERE_STORMS"."STORMS_1995"
),
yearly_counts AS (
    SELECT event_year AS year, "state", COUNT(*) AS event_count
    FROM all_events
    WHERE event_year BETWEEN 1980 AND 1995
    GROUP BY event_year, "state"
),
top_states_per_year AS (
    SELECT year, "state", event_count, ROW_NUMBER() OVER (PARTITION BY year ORDER BY event_count DESC) AS rn
    FROM yearly_counts
)
SELECT "state", SUM(event_count) AS total_events
FROM top_states_per_year
WHERE rn <= 1000
GROUP BY "state"
ORDER BY total_events DESC
LIMIT 5