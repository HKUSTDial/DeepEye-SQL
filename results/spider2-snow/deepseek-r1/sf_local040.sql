SELECT
    "MODERN_DATA"."MODERN_DATA"."TREES"."boroname",
    COUNT("MODERN_DATA"."MODERN_DATA"."TREES"."tree_id") AS tree_count,
    AVG("MODERN_DATA"."MODERN_DATA"."INCOME_TREES"."Estimate_Mean_income") AS avg_mean_income
FROM "MODERN_DATA"."MODERN_DATA"."TREES"
INNER JOIN "MODERN_DATA"."MODERN_DATA"."INCOME_TREES"
    ON "MODERN_DATA"."MODERN_DATA"."TREES"."zipcode" = "MODERN_DATA"."MODERN_DATA"."INCOME_TREES"."zipcode"
WHERE "MODERN_DATA"."MODERN_DATA"."INCOME_TREES"."Estimate_Median_income" > 0
    AND "MODERN_DATA"."MODERN_DATA"."INCOME_TREES"."Estimate_Mean_income" > 0
    AND "MODERN_DATA"."MODERN_DATA"."TREES"."boroname" IS NOT NULL
GROUP BY "MODERN_DATA"."MODERN_DATA"."TREES"."boroname"
ORDER BY tree_count DESC
LIMIT 3