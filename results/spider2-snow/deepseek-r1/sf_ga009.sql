december_events AS (
    SELECT *, ROW_NUMBER() OVER (ORDER BY "USER_PSEUDO_ID", "EVENT_DATE") AS rn FROM all_events
),
flattened AS (
    SELECT 
        de.rn,
        de."USER_PSEUDO_ID",
        ep.value:key::STRING AS param_key,
        COALESCE(ep.value:value:string_value::STRING, CAST(ep.value:value:int_value AS STRING)) AS param_value
    FROM december_events de,
    LATERAL FLATTEN(INPUT => de."EVENT_PARAMS") ep
),
event_params AS (
    SELECT 
        rn,
        "USER_PSEUDO_ID",
        MAX(CASE WHEN param_key = 'ga_session_id' THEN param_value END) AS ga_session_id,
        MAX(CASE WHEN param_key = 'session_engaged' THEN param_value END) AS session_engaged
    FROM flattened
    GROUP BY rn, "USER_PSEUDO_ID"
),
engaged_sessions AS (
    SELECT DISTINCT "USER_PSEUDO_ID", ga_session_id
    FROM event_params
    WHERE session_engaged = '1' AND ga_session_id IS NOT NULL
),
user_session_counts AS (
    SELECT "USER_PSEUDO_ID", COUNT(*) AS engaged_sessions_count
    FROM engaged_sessions
    GROUP BY "USER_PSEUDO_ID"
)
SELECT AVG(engaged_sessions_count) AS avg_engaged_sessions_per_user
FROM user_session_counts