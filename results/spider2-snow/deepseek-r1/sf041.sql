WITH HOURS_CTE AS (
  SELECT 
    TIMESTAMPADD(HOUR, SEQ4(), '2022-10-01 00:00:00') AS "DATETIME"
  FROM TABLE(GENERATOR(ROWCOUNT => 24))
)
SELECT
  'ERCOT' AS "iso",
  h."DATETIME" AS "datetime",
  COALESCE(mt."TIMEZONE", 'CDT') AS "timezone",
  mt."DATETIME_UTC" AS "datetime_utc",
  COALESCE(mt."ONPEAK", 0) AS "onpeak",
  COALESCE(mt."OFFPEAK", 0) AS "offpeak",
  COALESCE(mt."WEPEAK", 0) AS "wepeak",
  COALESCE(mt."WDPEAK", 0) AS "wdpeak",
  COALESCE(mt."MARKETDAY", '2022-10-01 00:00:00') AS "marketday",
  pn."PNODENAME" AS "price_node_name",
  10000697078 AS "price_node_id",
  dp."DALMP" AS "dalmp",
  dp."RTLMP" AS "rtlmp",
  dol."OBJECTNAME" AS "load_zone_name",
  10000712973 AS "load_zone_id",
  lf."VALUE" AS "load_forecast",
  lf."PUBLISHDATE" AS "load_forecast_publish_date",
  dl."RTLOAD" AS "rtload",
  wf."VALUE" AS "wind_gen_forecast",
  wf."PUBLISHDATE" AS "wind_gen_forecast_publish_date",
  wa."VALUE" AS "wind_gen",
  sf."VALUE" AS "solar_gen_forecast",
  sf."PUBLISHDATE" AS "solar_gen_forecast_publish_date",
  sa."VALUE" AS "solar_gen",
  (COALESCE(lf."VALUE", 0) - (COALESCE(wf."VALUE", 0) + COALESCE(sf."VALUE", 0))) AS "net_load_forecast",
  (COALESCE(dl."RTLOAD", 0) - (COALESCE(wa."VALUE", 0) + COALESCE(sa."VALUE", 0))) AS "net_load_real_time"
FROM HOURS_CTE h
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."ISO_MARKET_TIMES_SAMPLE" mt 
  ON h."DATETIME" = mt."DATETIME" AND mt."ISO" = 'ERCOT'
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."DART_PRICES_SAMPLE" dp 
  ON h."DATETIME" = dp."DATETIME" AND dp."OBJECTID" = 10000697078 AND dp."ISO" = 'E'
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."TS_FORECAST_SAMPLE" lf 
  ON h."DATETIME" = lf."DATETIME" AND lf."OBJECTID" = 10000712973 AND lf."DATATYPEID" = 19060
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."TS_FORECAST_SAMPLE" wf 
  ON h."DATETIME" = wf."DATETIME" AND wf."OBJECTID" = 10000712973 AND wf."DATATYPEID" = 9285
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."TS_FORECAST_SAMPLE" sf 
  ON h."DATETIME" = sf."DATETIME" AND sf."OBJECTID" = 10000712973 AND sf."DATATYPEID" = 662
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."DART_LOADS_SAMPLE" dl 
  ON h."DATETIME" = dl."DATETIME" AND dl."OBJECTID" = 10000712973
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."TS_GEN_SAMPLE" wa 
  ON h."DATETIME" = wa."DATETIME" AND wa."OBJECTID" = 10000712973 AND wa."DATATYPEID" = 16
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."TS_GEN_SAMPLE" sa 
  ON h."DATETIME" = sa."DATETIME" AND sa."OBJECTID" = 10000712973 AND sa."DATATYPEID" = 650
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."PRICE_NODES_SAMPLE" pn 
  ON pn."OBJECTID" = 10000697078 AND pn."ISO" = 'ERCOT'
LEFT JOIN "YES_ENERGY__SAMPLE_DATA"."YES_ENERGY_SAMPLE"."DS_OBJECT_LIST_SAMPLE" dol 
  ON dol."OBJECTID" = 10000712973 AND dol."OBJECTTYPE" = 'load_zone'
ORDER BY h."DATETIME"