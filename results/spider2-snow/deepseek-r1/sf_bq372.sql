WITH lost_orders AS (
  SELECT 
    "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_ORDERS"."OrderID",
    "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_ORDERS"."CustomerID"
  FROM "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_ORDERS"
  WHERE "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_ORDERS"."OrderID" NOT IN (
    SELECT DISTINCT "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_INVOICES"."OrderID"
    FROM "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_INVOICES"
  )
),
lost_order_values AS (
  SELECT 
    lost_orders."OrderID",
    lost_orders."CustomerID",
    SUM("WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_ORDERLINES"."UnitPrice" * "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_ORDERLINES"."Quantity" * (1 + "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_ORDERLINES"."TaxRate"/100)) AS order_value
  FROM lost_orders
  JOIN "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_ORDERLINES" ON lost_orders."OrderID" = "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_ORDERLINES"."OrderID"
  GROUP BY lost_orders."OrderID", lost_orders."CustomerID"
),
per_category_max AS (
  SELECT 
    "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_CUSTOMERCATEGORIES"."CustomerCategoryID",
    "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_CUSTOMERCATEGORIES"."CustomerCategoryName",
    MAX(lost_order_values.order_value) AS max_lost_order_value
  FROM lost_order_values
  JOIN "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_CUSTOMERS" ON lost_order_values."CustomerID" = "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_CUSTOMERS"."CustomerID"
  JOIN "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_CUSTOMERCATEGORIES" ON "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_CUSTOMERS"."CustomerCategoryID" = "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_CUSTOMERCATEGORIES"."CustomerCategoryID"
  GROUP BY "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_CUSTOMERCATEGORIES"."CustomerCategoryID", "WIDE_WORLD_IMPORTERS"."WIDE_WORLD_IMPORTERS"."SALES_CUSTOMERCATEGORIES"."CustomerCategoryName"
),
overall_avg AS (
  SELECT AVG(max_lost_order_value) AS avg_max_lost
  FROM per_category_max
)
SELECT 
  per_category_max."CustomerCategoryName"
FROM per_category_max
CROSS JOIN overall_avg
ORDER BY ABS(per_category_max.max_lost_order_value - overall_avg.avg_max_lost)
LIMIT 1