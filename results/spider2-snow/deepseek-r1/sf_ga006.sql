purchase_events AS (
    SELECT 
        e."USER_PSEUDO_ID",
        COALESCE(ep.value:"value"."string_value"::STRING, CAST(ep.value:"value"."int_value" AS STRING)) AS ga_session_id,
        e."ECOMMERCE":purchase_revenue_in_usd::FLOAT AS purchase_revenue
    FROM november_events e,
    LATERAL FLATTEN(INPUT => e."EVENT_PARAMS") ep
    WHERE e."EVENT_NAME" = 'purchase'
        AND e."ECOMMERCE":purchase_revenue_in_usd IS NOT NULL
        AND ep.value:"key"::STRING = 'ga_session_id'
        AND ga_session_id IS NOT NULL
),
session_revenue AS (
    SELECT 
        "USER_PSEUDO_ID",
        ga_session_id,
        SUM(purchase_revenue) AS session_revenue
    FROM purchase_events
    GROUP BY "USER_PSEUDO_ID", ga_session_id
),
user_metrics AS (
    SELECT 
        "USER_PSEUDO_ID",
        SUM(session_revenue) AS total_revenue,
        COUNT(*) AS session_count
    FROM session_revenue
    GROUP BY "USER_PSEUDO_ID"
    HAVING COUNT(*) > 1
)
SELECT 
    "USER_PSEUDO_ID",
    total_revenue / session_count AS avg_purchase_revenue_per_session
FROM user_metrics
ORDER BY "USER_PSEUDO_ID"