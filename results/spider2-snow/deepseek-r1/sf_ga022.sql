retention_flags AS (
  SELECT 
    "user_pseudo_id",
    cohort_date,
    MAX(CASE WHEN DATEDIFF(day, cohort_date, activity_date) BETWEEN 1 AND 7 THEN 1 ELSE 0 END) AS retained_week1,
    MAX(CASE WHEN DATEDIFF(day, cohort_date, activity_date) BETWEEN 8 AND 14 THEN 1 ELSE 0 END) AS retained_week2,
    MAX(CASE WHEN DATEDIFF(day, cohort_date, activity_date) BETWEEN 15 AND 21 THEN 1 ELSE 0 END) AS retained_week3
  FROM user_activity
  GROUP BY "user_pseudo_id", cohort_date
)
SELECT 
  COUNT(DISTINCT "user_pseudo_id") AS cohort_size,
  SUM(retained_week1) AS week1_retained_count,
  SUM(retained_week2) AS week2_retained_count,
  SUM(retained_week3) AS week3_retained_count,
  ROUND(SUM(retained_week1) * 100.0 / COUNT(DISTINCT "user_pseudo_id"), 2) AS week1_retention_rate,
  ROUND(SUM(retained_week2) * 100.0 / COUNT(DISTINCT "user_pseudo_id"), 2) AS week2_retention_rate,
  ROUND(SUM(retained_week3) * 100.0 / COUNT(DISTINCT "user_pseudo_id"), 2) AS week3_retention_rate
FROM retention_flags