user_first_session AS (
    SELECT DISTINCT 
        "user_pseudo_id",
        MIN(TO_DATE("event_date", 'YYYYMMDD')) AS first_session_date
    FROM all_events
    GROUP BY "user_pseudo_id"
),
cohort_assignments AS (
    SELECT 
        "user_pseudo_id",
        first_session_date,
        DATE_TRUNC('WEEK', first_session_date) AS cohort_week_monday
    FROM user_first_session
    WHERE first_session_date >= '2018-07-02'
),
user_week4_activity AS (
    SELECT 
        ca."user_pseudo_id",
        ca.cohort_week_monday,
        CASE 
            WHEN EXISTS (
                SELECT 1 
                FROM all_events ae
                WHERE ae."user_pseudo_id" = ca."user_pseudo_id"
                  AND TO_DATE(ae."event_date", 'YYYYMMDD') BETWEEN DATEADD(DAY, 21, ca.first_session_date) AND DATEADD(DAY, 27, ca.first_session_date)
            ) THEN 1 
            ELSE 0 
        END AS returned_in_week4
    FROM cohort_assignments ca
),
cohort_retention AS (
    SELECT 
        cohort_week_monday,
        COUNT(DISTINCT "user_pseudo_id") AS cohort_size,
        SUM(returned_in_week4) AS returned_count,
        (SUM(returned_in_week4) * 100.0 / COUNT(DISTINCT "user_pseudo_id")) AS retention_rate
    FROM user_week4_activity
    GROUP BY cohort_week_monday
)
SELECT 
    TO_CHAR(cohort_week_monday, 'YYYY-MM-DD') AS cohort_monday
FROM cohort_retention
ORDER BY retention_rate DESC
LIMIT 1