SELECT "STACKOVERFLOW"."STACKOVERFLOW"."USERS"."display_name"
FROM "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS"
JOIN "STACKOVERFLOW"."STACKOVERFLOW"."USERS" ON "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS"."owner_user_id" = "STACKOVERFLOW"."STACKOVERFLOW"."USERS"."id"
WHERE "STACKOVERFLOW"."STACKOVERFLOW"."USERS"."reputation" > 10 AND "STACKOVERFLOW"."STACKOVERFLOW"."POSTS_ANSWERS"."owner_user_id" IS NOT NULL
GROUP BY "STACKOVERFLOW"."STACKOVERFLOW"."USERS"."display_name"
ORDER BY COUNT(*) DESC
LIMIT 1