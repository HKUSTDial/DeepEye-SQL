SELECT ca."tract_ce"
FROM "CENSUS_BUREAU_ACS_1"."GEO_CENSUS_TRACTS"."CENSUS_TRACTS_CALIFORNIA" ca
JOIN "CENSUS_BUREAU_ACS_1"."CENSUS_BUREAU_ACS"."CENSUSTRACT_2015_5YR" i15 ON ca."geo_id" = i15."geo_id"
JOIN "CENSUS_BUREAU_ACS_1"."CENSUS_BUREAU_ACS"."CENSUSTRACT_2018_5YR" i18 ON ca."geo_id" = i18."geo_id"
ORDER BY (i18."median_income" - i15."median_income") DESC
LIMIT 1