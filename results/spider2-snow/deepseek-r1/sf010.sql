WITH california_geos AS (
  SELECT "GEO_ID" FROM "US_REAL_ESTATE"."CYBERSYN"."GEOGRAPHY_INDEX" 
  WHERE "GEO_NAME" = 'California' AND "LEVEL" = 'State'
  UNION
  SELECT "RELATED_GEO_ID" AS "GEO_ID" 
  FROM "US_REAL_ESTATE"."CYBERSYN"."GEOGRAPHY_RELATIONSHIPS" 
  WHERE "GEO_ID" IN (SELECT "GEO_ID" FROM "US_REAL_ESTATE"."CYBERSYN"."GEOGRAPHY_INDEX" WHERE "GEO_NAME" = 'California' AND "LEVEL" = 'State')
    AND "RELATIONSHIP_TYPE" = 'Contains'
),
default_variables AS (
  SELECT "VARIABLE" 
  FROM "US_REAL_ESTATE"."CYBERSYN"."FHFA_MORTGAGE_PERFORMANCE_ATTRIBUTES" 
  WHERE "VARIABLE_GROUP" IN ('Percent 90 to 180 Days Past Due Date', 'Percent in Forbearance')
     OR "VARIABLE_NAME" ILIKE '%foreclosure%'
     OR "VARIABLE_NAME" ILIKE '%bankruptcy%'
     OR "VARIABLE_NAME" ILIKE '%deed-in-lieu%'
)
SELECT 
  "DATE",
  SUM("VALUE") AS "CUMULATIVE_RATIO"
FROM "US_REAL_ESTATE"."CYBERSYN"."FHFA_MORTGAGE_PERFORMANCE_TIMESERIES"
WHERE EXTRACT(YEAR FROM "DATE") = 2023
  AND "GEO_ID" IN (SELECT "GEO_ID" FROM california_geos)
  AND "VARIABLE" IN (SELECT "VARIABLE" FROM default_variables)
GROUP BY "DATE"
ORDER BY "DATE"