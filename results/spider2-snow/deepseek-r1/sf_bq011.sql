WITH users_7day AS (
  SELECT DISTINCT "USER_PSEUDO_ID"
  FROM (
    SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20210101"
    UNION ALL
    SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20210102"
    UNION ALL
    SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20210103"
    UNION ALL
    SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20210104"
    UNION ALL
    SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20210105"
    UNION ALL
    SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20210106"
    UNION ALL
    SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20210107"
  ) AS events,
  LATERAL FLATTEN(INPUT => events."EVENT_PARAMS") AS param
  WHERE param.key = 'engagement_time_msec'
    AND param.value:"int_value" > 0
    AND events."EVENT_TIMESTAMP" BETWEEN 1609459200000000 AND 1610063999000000
),
users_2day AS (
  SELECT DISTINCT "USER_PSEUDO_ID"
  FROM (
    SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20210106"
    UNION ALL
    SELECT * FROM "GA4"."GA4_OBFUSCATED_SAMPLE_ECOMMERCE"."EVENTS_20210107"
  ) AS events,
  LATERAL FLATTEN(INPUT => events."EVENT_PARAMS") AS param
  WHERE param.key = 'engagement_time_msec'
    AND param.value:"int_value" > 0
    AND events."EVENT_TIMESTAMP" BETWEEN 1609891200000000 AND 1610063999000000
)
SELECT COUNT(DISTINCT u7."USER_PSEUDO_ID") AS user_count
FROM users_7day u7
LEFT JOIN users_2day u2 ON u7."USER_PSEUDO_ID" = u2."USER_PSEUDO_ID"
WHERE u2."USER_PSEUDO_ID" IS NULL