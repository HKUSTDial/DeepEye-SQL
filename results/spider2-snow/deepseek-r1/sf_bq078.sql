WITH ALL_ASSOCIATIONS AS (
    SELECT "targetId", "score" FROM "OPEN_TARGETS_PLATFORM_2"."OPEN_TARGETS_PLATFORM"."ASSOCIATIONBYDATASOURCEDIRECT" WHERE "diseaseId" = 'EFO_0000676' AND LOWER("datasourceId") = 'impc'
    UNION ALL
    SELECT "targetId", "score" FROM "OPEN_TARGETS_PLATFORM_2"."OPEN_TARGETS_PLATFORM"."ASSOCIATIONBYDATASOURCEINDIRECT" WHERE "diseaseId" = 'EFO_0000676' AND LOWER("datasourceId") = 'impc'
),
RANKED AS (
    SELECT "targetId", "score", RANK() OVER (ORDER BY "score" DESC) AS rank_num FROM ALL_ASSOCIATIONS
)
