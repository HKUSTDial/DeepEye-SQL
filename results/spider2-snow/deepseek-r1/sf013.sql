SELECT
  "CLASS" AS "class",
  "SUBCLASS" AS "subclass",
  SUM(CASE WHEN STARTSWITH("QUADKEY", '12020210') THEN CAST("LENGTH_M" AS DECIMAL(10,2)) ELSE 0 END) AS "Amsterdam_length",
  SUM(CASE WHEN STARTSWITH("QUADKEY", '12020211') THEN CAST("LENGTH_M" AS DECIMAL(10,2)) ELSE 0 END) AS "Rotterdam_length"
FROM "NETHERLANDS_OPEN_MAP_DATA"."NETHERLANDS"."V_ROAD"
WHERE STARTSWITH("QUADKEY", '12020210') OR STARTSWITH("QUADKEY", '12020211')
GROUP BY "CLASS", "SUBCLASS"
ORDER BY "CLASS", "SUBCLASS"