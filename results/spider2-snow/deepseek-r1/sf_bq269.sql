session_data AS (
  SELECT
    "fullVisitorId",
    "date",
    "totals":pageviews::INTEGER AS pageviews,
    "totals":transactions::INTEGER AS transactions
  FROM all_sessions
  WHERE "totals":pageviews IS NOT NULL
    AND "date" BETWEEN '20170601' AND '20170731'
),
classified_sessions AS (
  SELECT
    "fullVisitorId",
    "date",
    pageviews,
    CASE WHEN COALESCE(transactions, 0) > 0 THEN 'purchase' ELSE 'non_purchase' END AS session_type
  FROM session_data
),
visitor_monthly_totals AS (
  SELECT
    LEFT("date", 6) AS month,
    "fullVisitorId",
    session_type,
    SUM(pageviews) AS total_pageviews
  FROM classified_sessions
  GROUP BY month, "fullVisitorId", session_type
)
SELECT
  month,
  AVG(CASE WHEN session_type = 'purchase' THEN total_pageviews END) AS avg_pageviews_purchase,
  AVG(CASE WHEN session_type = 'non_purchase' THEN total_pageviews END) AS avg_pageviews_non_purchase
FROM visitor_monthly_totals
GROUP BY month
ORDER BY month