WITH PATENT_APP AS (
    SELECT 
        P."id" AS PATENT_ID,
        P."title" AS PATENT_TITLE,
        TRY_TO_DATE(A."date") AS APPLICATION_DATE
    FROM 
        "PATENTSVIEW"."PATENTSVIEW"."PATENT" P
    INNER JOIN 
        "PATENTSVIEW"."PATENTSVIEW"."APPLICATION" A ON P."id" = A."patent_id"
    WHERE 
        P."country" = 'US' 
        AND A."country" = 'US'
        AND TRY_TO_DATE(A."date") IS NOT NULL
),
PATENT_CPC AS (
    SELECT DISTINCT 
        PA.*
    FROM 
        PATENT_APP PA
    INNER JOIN 
        "PATENTSVIEW"."PATENTSVIEW"."CPC_CURRENT" CPC ON PA.PATENT_ID = CPC."patent_id"
    WHERE 
        CPC."section_id" = 'C'
),
BACKWARD_CITES AS (
    SELECT 
        PC.PATENT_ID,
        COUNT(DISTINCT USC."citation_id") AS BACKWARD_CITATION_COUNT
    FROM 
        PATENT_CPC PC
    INNER JOIN 
        "PATENTSVIEW"."PATENTSVIEW"."USPATENTCITATION" USC ON PC.PATENT_ID = USC."patent_id"
    WHERE 
        USC."country" = 'US'
        AND TRY_TO_DATE(USC."date") IS NOT NULL
        AND TRY_TO_DATE(USC."date") >= DATEADD(YEAR, -1, PC.APPLICATION_DATE)
        AND TRY_TO_DATE(USC."date") < PC.APPLICATION_DATE
    GROUP BY 
        PC.PATENT_ID
    HAVING 
        COUNT(DISTINCT USC."citation_id") > 0
),
FORWARD_CITES_1YR AS (
    SELECT 
        PC.PATENT_ID,
        COUNT(DISTINCT USC."patent_id") AS FORWARD_CITATION_1YR_COUNT
    FROM 
        PATENT_CPC PC
    INNER JOIN 
        "PATENTSVIEW"."PATENTSVIEW"."USPATENTCITATION" USC ON PC.PATENT_ID = USC."citation_id"
    WHERE 
        USC."country" = 'US'
        AND TRY_TO_DATE(USC."date") IS NOT NULL
        AND TRY_TO_DATE(USC."date") > PC.APPLICATION_DATE
        AND TRY_TO_DATE(USC."date") <= DATEADD(YEAR, 1, PC.APPLICATION_DATE)
    GROUP BY 
        PC.PATENT_ID
    HAVING 
        COUNT(DISTINCT USC."patent_id") > 0
),
FORWARD_CITES_3YR AS (
    SELECT 
        PC.PATENT_ID,
        COUNT(DISTINCT USC."patent_id") AS FORWARD_CITATION_3YR_COUNT
    FROM 
        PATENT_CPC PC
    INNER JOIN 
        "PATENTSVIEW"."PATENTSVIEW"."USPATENTCITATION" USC ON PC.PATENT_ID = USC."citation_id"
    WHERE 
        USC."country" = 'US'
        AND TRY_TO_DATE(USC."date") IS NOT NULL
        AND TRY_TO_DATE(USC."date") > PC.APPLICATION_DATE
        AND TRY_TO_DATE(USC."date") <= DATEADD(YEAR, 3, PC.APPLICATION_DATE)
    GROUP BY 
        PC.PATENT_ID
)
SELECT 
    PC.PATENT_ID,
    PC.PATENT_TITLE,
    PC.APPLICATION_DATE,
    BC.BACKWARD_CITATION_COUNT,
    FC3.FORWARD_CITATION_3YR_COUNT
FROM 
    PATENT_CPC PC
INNER JOIN 
    BACKWARD_CITES BC ON PC.PATENT_ID = BC.PATENT_ID
INNER JOIN 
    FORWARD_CITES_1YR FC1 ON PC.PATENT_ID = FC1.PATENT_ID
LEFT JOIN 
    FORWARD_CITES_3YR FC3 ON PC.PATENT_ID = FC3.PATENT_ID
ORDER BY 
    BC.BACKWARD_CITATION_COUNT DESC
LIMIT 1