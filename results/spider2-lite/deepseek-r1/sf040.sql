WITH largest_zip AS (
    SELECT "ZIP"
    FROM "US_ADDRESSES__POI"."CYBERSYN"."US_ADDRESSES"
    WHERE "STATE" = 'FL'
    GROUP BY "ZIP"
    ORDER BY COUNT(*) DESC
    LIMIT 1
)
SELECT "NUMBER", "STREET", "STREET_TYPE"
FROM "US_ADDRESSES__POI"."CYBERSYN"."US_ADDRESSES"
WHERE "STATE" = 'FL'
  AND "ZIP" = (SELECT "ZIP" FROM largest_zip)
ORDER BY "LATITUDE" DESC
LIMIT 10