WITH AMSTERDAM_ROADS AS (
  SELECT "CLASS", "SUBCLASS", SUM(CAST("LENGTH_M" AS FLOAT)) AS "Amsterdam's road lengths"
  FROM "NETHERLANDS_OPEN_MAP_DATA"."NETHERLANDS"."V_ROAD"
  WHERE "QUADKEY" LIKE '12020210%'
  GROUP BY "CLASS", "SUBCLASS"
),
ROTTERDAM_ROADS AS (
  SELECT "CLASS", "SUBCLASS", SUM(CAST("LENGTH_M" AS FLOAT)) AS "Rotterdam's road lengths"
  FROM "NETHERLANDS_OPEN_MAP_DATA"."NETHERLANDS"."V_ROAD"
  WHERE "QUADKEY" LIKE '12020211%'
  GROUP BY "CLASS", "SUBCLASS"
)
SELECT COALESCE(A."CLASS", R."CLASS") AS "CLASS",
       COALESCE(A."SUBCLASS", R."SUBCLASS") AS "SUBCLASS",
       COALESCE(A."Amsterdam's road lengths", 0) AS "Amsterdam's road lengths",
       COALESCE(R."Rotterdam's road lengths", 0) AS "Rotterdam's road lengths"
FROM AMSTERDAM_ROADS A
FULL OUTER JOIN ROTTERDAM_ROADS R ON A."CLASS" = R."CLASS" AND A."SUBCLASS" = R."SUBCLASS"
ORDER BY "CLASS", "SUBCLASS"