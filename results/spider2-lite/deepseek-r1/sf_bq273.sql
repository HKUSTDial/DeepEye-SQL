WITH monthly_profits AS (
    SELECT 
        DATE_TRUNC('month', TO_TIMESTAMP("THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDER_ITEMS"."delivered_at" / 1000000)) AS delivery_month,
        SUM("THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDER_ITEMS"."sale_price" - "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."INVENTORY_ITEMS"."cost") AS monthly_profit
    FROM "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDER_ITEMS"
    JOIN "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDERS" ON "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDER_ITEMS"."order_id" = "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDERS"."order_id"
    JOIN "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."USERS" ON "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDERS"."user_id" = "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."USERS"."id"
    JOIN "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."INVENTORY_ITEMS" ON "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDER_ITEMS"."inventory_item_id" = "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."INVENTORY_ITEMS"."id"
    WHERE "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."USERS"."traffic_source" = 'Facebook'
        AND "THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDER_ITEMS"."status" = 'Complete'
        AND TO_TIMESTAMP("THELOOK_ECOMMERCE"."THELOOK_ECOMMERCE"."ORDERS"."created_at" / 1000000) BETWEEN '2022-08-01' AND '2023-11-30'
    GROUP BY delivery_month
),
monthly_changes AS (
    SELECT 
        delivery_month,
        monthly_profit,
        monthly_profit - LAG(monthly_profit) OVER (ORDER BY delivery_month) AS profit_increase
    FROM monthly_profits
    WHERE delivery_month BETWEEN '2022-08-01' AND '2023-11-01'
)
SELECT 
    delivery_month,
    profit_increase
FROM monthly_changes
WHERE profit_increase IS NOT NULL
ORDER BY profit_increase DESC
LIMIT 5