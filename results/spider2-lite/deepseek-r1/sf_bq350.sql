SELECT DISTINCT "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"."id", "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"."drugType", "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"."hasBeenWithdrawn"
FROM "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"
WHERE "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"."isApproved" = TRUE
AND "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"."blackBoxWarning" = TRUE
AND "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."MOLECULE"."id" IN (
  SELECT DISTINCT "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"."drugId"
  FROM "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"
  LEFT JOIN LATERAL FLATTEN(INPUT => "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"."tradeNames"['list']) trade_name
  WHERE "OPEN_TARGETS_PLATFORM_1"."PLATFORM"."KNOWNDRUGSAGGREGATED"."prefName" IN ('Keytruda', 'Vioxx', 'Premarin', 'Humira')
     OR trade_name.VALUE::STRING IN ('Keytruda', 'Vioxx', 'Premarin', 'Humira')
)